quote = "Nic co ludzkie, nie jest mi obce."

print("Oryginalny cytat:")
print(quote)

print("\nDużymi literami:")
print(quote.upper())

print("\nMałymi literami:")
print(quote.lower())

print("\nWszystkie wyrazy od dużej litery:")
print(quote.title())

print("\nZ drobną zmianą:")
print(quote.replace("obce", "straszne"))

print("\nOryginalny tekst bez zmian")
print(quote)

